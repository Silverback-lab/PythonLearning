import os
os.system('clear')

my_string = " my name is Michael Zeisel"

# prints all upper .upper()
print(my_string.upper())
# prints all lower .lower()
print(my_string.lower())
# prints first letter capitol .capitalize()
print(my_string.capitalize())
# prints camel case.title()
print(my_string.title())
# prints swap case .swapcase()
print(my_string.swapcase())


# len wil count length of string
print (len(my_string))

